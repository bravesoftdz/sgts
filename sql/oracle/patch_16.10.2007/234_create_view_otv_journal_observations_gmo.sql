/* Создание просмотра Отвесов с Гидрометеорологией в журнале наблюдений */

CREATE OR REPLACE VIEW S_OTV_JOURNAL_OBSERVATIONS_GMO
AS
  SELECT OTV.*,
         GMO.UVB, 
		 GMO.UNB,
		 GMO.T_AIR,
		 GMO.T_WATER,
		 GMO.RAIN_DAY,
		 GMO.PREC,
		 GMO.PREC_NAME,
		 GMO.UNSET,
		 GMO.INFLUX,
		 GMO.V_VAULT,
		 GMO.UVB_INC,
		 GMO.RAIN_YEAR,
		 GMO.T_AIR_10,
		 GMO.T_AIR_30
    FROM S_OTV_JOURNAL_OBSERVATIONS OTV, S_GMO_JOURNAL_OBSERVATIONS GMO
   WHERE OTV.DATE_OBSERVATION=GMO.DATE_OBSERVATION (+)

