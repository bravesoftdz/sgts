/* Создание просмотра Плотина в журнале наблюдений старых данных */

CREATE MATERIALIZED VIEW S_SLF_JOURNAL_OBSERVATIONS_O1
NOLOGGING
NOCACHE
NOPARALLEL
BUILD DEFERRED
REFRESH COMPLETE
START WITH TO_DATE('01.06.2007','DD.MM.YYYY')
DISABLE QUERY REWRITE AS
SELECT * FROM TABLE(GET_SLF_JOURNAL_OBSERVATIONS(30010,1))

--

