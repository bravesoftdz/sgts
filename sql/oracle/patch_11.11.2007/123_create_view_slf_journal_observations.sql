--

/* Создание индекса на вид измерения просмотра Плотина в журнале наблюдений старых данных */

CREATE INDEX IDX_SLF_JO_O1_3
 ON S_SLF_JOURNAL_OBSERVATIONS_O1(MEASURE_TYPE_ID)

--

/* Создание просмотра Плотина в журнале наблюдений новых данных */

CREATE OR REPLACE VIEW S_SLF_JOURNAL_OBSERVATIONS_N1
AS
  SELECT * FROM TABLE(GET_SLF_JOURNAL_OBSERVATIONS(30010,0))

--

/* Создание просмотра Плотина в журнале наблюдений */

CREATE OR REPLACE VIEW S_SLF_JOURNAL_OBSERVATIONS_1
AS
  SELECT JOO1.*
    FROM S_SLF_JOURNAL_OBSERVATIONS_O1 JOO1
   UNION
  SELECT JON1.*
    FROM S_SLF_JOURNAL_OBSERVATIONS_N1 JON1

