/* BY SYS */

DROP USER SGTS;

--

CREATE USER SGTS  
PROFILE "DEFAULT"
IDENTIFIED BY "1"
DEFAULT TABLESPACE "USERS"
ACCOUNT UNLOCK;

--

GRANT UNLIMITED TABLESPACE TO SGTS;

--

GRANT CREATE ANY PROCEDURE TO SGTS;

--

GRANT CONNECT TO SGTS;

--

GRANT CREATE ANY TYPE TO SGTS;

--

GRANT CREATE ANY VIEW TO SGTS;

--

GRANT CREATE ANY SNAPSHOT TO SGTS;

--

GRANT QUERY REWRITE TO SGTS;

--

COMMIT;